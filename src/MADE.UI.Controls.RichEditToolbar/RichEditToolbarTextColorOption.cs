namespace MADE.UI.Controls
{
    /// <summary>
    /// Defines a text color option for the <see cref="RichEditToolbar"/>.
    /// </summary>
    public class RichEditToolbarTextColorOption
    {
        /// <summary>
        /// Gets or sets the name of the color.
        /// </summary>
        public string Name { get; set; }

